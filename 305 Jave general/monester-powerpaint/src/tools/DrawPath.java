package tools;

import java.awt.Color;
import java.awt.Shape;

/**
 * A Class stored information of a drawing path.
 * 
 * @author richardyang
 * @version 11.16
 */
public class DrawPath {

    /** The drawing path. */
    private final Shape myPath;
    
    /** The color of the path. */
    private final Color myDrawColor;
    
    /** The fill color of the path. */
    private final Color myFillColor;
    
    /** The thickness of the path. */
    private final int myLineWidth;
    
    /** The fillable. */
    private final boolean myFillable;
    
    /**
     * The constructor for Drawing path.
     * 
     * @param thePath drawing path info
     * @param theDrawColor drawing color
     * @param theFillColor fill color
     * @param theLineWidth the thickness of the path
     * @param theFillable the fillable
     */
    public DrawPath(final Shape thePath, final Color theDrawColor, final Color theFillColor,
                    final int theLineWidth, final boolean theFillable) {
        myPath = thePath;
        myDrawColor = theDrawColor;
        myFillColor = theFillColor;
        myLineWidth = theLineWidth;
        myFillable = theFillable;
    }

    /**
     * getter for drawing color.
     * 
     * @return the draw color
     */
    public Color getColor() {
        return myDrawColor;
    }
    
    /**
     * getter for fill color.
     * 
     * @return the fill color
     */
    public Color getFillColor() {
        return myFillColor;
    }

    /**
     * getter for the shape path.
     * 
     * @return the draw path
     */
    public Shape getPath() {
        return myPath;
    }
    
    /**
     * getter for the thickness.
     * 
     * @return the thickness
     */
    public int getLineWidth() {
        return myLineWidth;
    }
    
    /**
     * getter for the fillable.
     * 
     * @return the fillable
     */
    public boolean isFillable() {
        return myFillable;
    }
    

}
